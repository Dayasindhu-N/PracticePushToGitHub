package arrayOfObject_programs;

public class Unboxing {
	public static void main(String[] args) {
		Integer a=10;
		int b=a.intValue();
		System.out.println(b);
		
		Character c='D';
		char ch=c.charValue();
		System.out.println(ch);
		
		Boolean d=true;
		boolean e=d.booleanValue();
		System.out.println(e);
	}
}
